def divide_by_three(num):
    if num % 3 == 0:
        return "Fizz"
    else:
        return ""

def divide_by_five(num):
    if num % 5 == 0:
        return "Buzz"
    else:
        return ""
    
def divide_by_three_and_five(num):
    if num % 3 == 0 and num % 5 ==0:
        return "FizzBuzz"
    else:
        return ""

        
